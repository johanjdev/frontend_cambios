function setLanguage(lang) {
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach((element) => {
    const key = element.dataset.i18n;
    if (!translations[lang] || !translations[lang][key]) return;
    element.innerHTML = translations[lang][key];
  });

  const titleElements = document.querySelectorAll('[data-i18n-title]');
  titleElements.forEach((element) => {
    const key = element.dataset.i18nTitle;
    if (!translations[lang] || !translations[lang][key]) return;
    const val = translations[lang][key];
    element.dataset.title = val;

    // If the element wraps an image (gallery item), update its caption element instead
    if (element.querySelector && element.querySelector('img')) {
      const caption = element.querySelector('.overlay-title') || element.querySelector('.img-label');
      if (caption) caption.innerHTML = val;
    } else {
      element.innerHTML = val;
    }
  });

  const descElements = document.querySelectorAll('[data-i18n-desc]');
  descElements.forEach((element) => {
    const key = element.dataset.i18nDesc;
    if (!translations[lang] || !translations[lang][key]) return;
    const val = translations[lang][key];
    element.dataset.desc = val;
    if (element.querySelector && element.querySelector('img')) {
      const caption = element.querySelector('.img-label') || element.querySelector('.overlay-desc');
      if (caption) caption.innerHTML = val;
    } else {
      if (!element.dataset.noInner) element.innerHTML = val;
    }
  });

  const buttons = document.querySelectorAll('.lang-toggle');
  buttons.forEach((button) => {
    button.textContent = lang === 'es' ? 'EN' : 'ES';
    button.setAttribute('aria-label', lang === 'es' ? 'Cambiar a inglés' : 'Switch to Spanish');
  });

  document.documentElement.lang = lang;
  localStorage.setItem('siteLang', lang);
}

function initLanguageToggle() {
  const savedLang = localStorage.getItem('siteLang');
  const htmlLang = document.documentElement.lang;
  let initialLang = savedLang || htmlLang || 'es';
  if (!['es', 'en'].includes(initialLang)) {
    initialLang = 'es';
  }
  setLanguage(initialLang);

  const buttons = document.querySelectorAll('.lang-toggle');
  buttons.forEach((button) => {
    button.addEventListener('click', () => {
      const newLang = document.documentElement.lang === 'en' ? 'es' : 'en';
      const langModalElement = document.getElementById('langModal');
      if (!langModalElement || typeof bootstrap === 'undefined') return;

      const langModal = new bootstrap.Modal(langModalElement);
      const confirmBtn = document.getElementById('confirmLangBtn');
      if (confirmBtn) {
        confirmBtn.dataset.newLang = newLang;
      }
      langModal.show();
    });
  });

  // handle confirm button if present
  const confirmBtnGlobal = document.getElementById('confirmLangBtn');
  if (confirmBtnGlobal) {
    confirmBtnGlobal.addEventListener('click', (e) => {
      const newLang = e.currentTarget.dataset.newLang;
      if (newLang) setLanguage(newLang);
      const modalEl = document.getElementById('langModal');
      const m = bootstrap.Modal.getInstance(modalEl);
      if (m) m.hide();
    });
  }
}

window.addEventListener('DOMContentLoaded', initLanguageToggle);
